package com.dolatkia.animatedThemeManager

class Coordinate {
    var left = 0
    var top = 0
    var width = 0
    var height = 0

    fun Coordinate(left: Int, top: Int, width: Int, height: Int) {
        this.left = left
        this.top = top
        this.width = width
        this.height = height
    }
}